def estimate_lunar_day(d) -> int:
    from datetime import datetime
    if not hasattr(d, 'year'):
        d = datetime.now()
    ref = datetime(2000, 1, 6)
    return (d - ref).days % 28 + 1

def get_solar_mansion(d) -> dict:
    from datetime import datetime
    if not hasattr(d, 'year'):
        d = datetime.now()
    spring = datetime(d.year, 2, 15)
    if d < spring:
        spring = datetime(d.year - 1, 2, 15)
    days_passed = (d - spring).days
    s = ((days_passed + 3) // 13 + 1)
    s = ((s - 1) % 28) + 1
    return LUNAR_MANSIONS.get(s, LUNAR_MANSIONS[1])