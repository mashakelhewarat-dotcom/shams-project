def get_wafq_from_zodiac(zodiac_name: str, base_value: int = 0) -> Optional[list]:
    z = ZODIAC.get(zodiac_name)
    if not z:
        return None
    wtype = z.get('wafq_type', '3x3')
    size = int(wtype[0]) if wtype and wtype[0].isdigit() else 3
    magic = [[0] * size for _ in range(size)]
    r, c = 0, size // 2
    for n in range(1, size * size + 1):
        magic[r][c] = n + base_value
        nr, nc = (r - 1) % size, (c + 1) % size
        if magic[nr][nc] != 0:
            r = (r + 1) % size
        else:
            r, c = nr, nc
    return magic

def generate_wafq_by_type(wtype: str, base: int = 0) -> list:
    size_map = {'3x3':3, '4x4':4, '5x5':5, '6x6':6, '7x7':7, '8x8':8, '9x9':9}
    size = size_map.get(wtype, 3)
    magic = [[0] * size for _ in range(size)]
    if size % 2 == 1:
        r, c = 0, size // 2
        for n in range(1, size * size + 1):
            magic[r][c] = n + base
            nr, nc = (r - 1) % size, (c + 1) % size
            if magic[nr][nc] != 0:
                r = (r + 1) % size
            else:
                r, c = nr, nc
    else:
        if size == 4:
            magic = [[16,2,3,13],[5,11,10,8],[9,7,6,12],[4,14,15,1]]
            magic = [[v + base for v in row] for row in magic]
        else:
            for i in range(size):
                for j in range(size):
                    magic[i][j] = i * size + j + 1 + base
    return magic

