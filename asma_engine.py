def suggest_asma_for_intent(intent: str, limit: int = 5) -> list:
    """
    ترجع قائمة بالأسماء المناسبة للنية مع تفاصيلها الكاملة
    """
    names = INTENT_TO_ASMA.get(intent, [])[:limit]
    result = []
    for name in names:
        data = ASMA_99.get(name, {})
        if data:
            result.append({
                'name': name,
                'abjad': data.get('abjad'),
                'planet': data.get('planet'),
                'day': data.get('day'),
                'material': data.get('material'),
                'incense': data.get('incense'),
                'wafq': data.get('wafq'),
                'note': data.get('note'),
                'psychology': data.get('psychology'),
                'repeat_count': data.get('repeat_count'),
                'warning': data.get('warning'),
            })
    return result


def get_asma_timing(name: str) -> dict:
    """ترجع أفضل توقيت فلكي لاستخدام الاسم"""
    data = ASMA_99.get(name, {})
    if not data:
        return {}
    return {
        'name': name,
        'planet': data.get('planet'),
        'day': data.get('day'),
        'hour': data.get('hour'),
        'exaltation': data.get('exaltation'),
        'best_time': f"{data.get('day', 'أي يوم')} في الساعة {data.get('hour', 'الأولى')} الفلكية" if data.get('day') else None,
    }


def get_asma_material(name: str) -> dict:
    """ترجع المادة والبخور المناسب للاسم"""
    data = ASMA_99.get(name, {})
    return {
        'name': name,
        'material': data.get('material', 'ورق أبيض'),
        'incense': data.get('incense', 'عود'),
        'wafq_size': data.get('wafq'),
        'wafq_shape': data.get('wafq_shape', 'square'),
    }


def get_asma_full(name: str) -> dict:
    """ترجع كامل بيانات الاسم"""
    data = ASMA_99.get(name, {})
    if not data:
        return {'error': f'الاسم "{name}" غير موجود في قاعدة البيانات'}
    return {'name': name, **data}


def get_all_asma_by_planet(planet: str) -> list:
    """ترجع كل الأسماء المرتبطة بكوكب معين"""
    return [
        {'name': name, **{k: v for k, v in data.items() if k in ['abjad', 'wafq', 'intent', 'note']}}
        for name, data in ASMA_99.items()
        if data.get('planet') == planet
    ]


def get_asma_with_warning() -> list:
    """ترجع الأسماء التي تتطلب تحذيراً أخلاقياً"""
    return [
        {'name': name, 'warning': data['warning'], 'intent': data.get('intent', [])}
        for name, data in ASMA_99.items()
        if data.get('warning')
    ]


def search_asma_by_intent_keyword(keyword: str) -> list:
    """بحث نصي في نوايا الأسماء"""
    results = []
    for name, data in ASMA_99.items():
        intents = data.get('intent', [])
        note = data.get('note', '')
        psychology = data.get('psychology', '') or ''
        if any(keyword in i for i in intents) or keyword in note or keyword in psychology:
            results.append({
                'name': name,
                'abjad': data.get('abjad'),
                'note': data.get('note'),
                'match_context': [i for i in intents if keyword in i],
            })
    return results


