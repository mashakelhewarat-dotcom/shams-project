# 9. ضغط الأعداد — الجمل الصغير
# ============================================================================
def small_abjad(n: int) -> int:
    if n <= 0:
        return 9
    while n > 9:
        n = sum(int(d) for d in str(n))
        if n == 0:
            n = 9
    return n

